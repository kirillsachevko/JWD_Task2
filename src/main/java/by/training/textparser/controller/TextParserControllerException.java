package by.training.textparser.controller;

public class TextParserControllerException extends Exception {

    public TextParserControllerException() {

    }

    public TextParserControllerException(String message) {
        super(message);
    }

    public TextParserControllerException(String message, Throwable cause) {
        super(message, cause);
    }

    public TextParserControllerException(Throwable cause) {
        super(cause);
    }
}
