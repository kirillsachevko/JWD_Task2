package by.training.textparser.dao;

public class TextParserDaoException extends Exception {

    public TextParserDaoException() {
    }

    public TextParserDaoException(String message) {
        super(message);
    }

    public TextParserDaoException(String message, Throwable cause) {
        super(message, cause);
    }

    public TextParserDaoException(Throwable cause) {
        super(cause);
    }
}
